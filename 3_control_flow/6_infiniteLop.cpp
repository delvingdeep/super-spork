/** Infinite loops are evitable for some
** ever-going application and so we can create
** one such using FOR and WHILE loops
*/

//using FOR loop
for( ; ;)
{
     std::cout<<"This for loop will run forever\n";
}

//using WHILE loop
while(1)
{
     std::cout<<"This while loop will run forever\n";
}

